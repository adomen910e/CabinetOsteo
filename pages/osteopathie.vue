            Déroulement d'une séance
        </h2>

        <p>
            Une séance dure en moyenne 45 minutes pour un cheval et 30 minutes pour un chien/chat.
            
            Je commence par l’anamnèse en m’adressant au propriétaire afin d’en savoir un peu plus sur l’animal et sur les raisons qui l’ont poussé à me contacter.
            
            Puis, j’effectue un examen dynamique de l’animal afin d’observer dans sa démarche certains signes sur les éventuelles dysfonctions.
            
