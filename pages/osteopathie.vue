            Le temps de repos et la rééducation qui suivent une séance ne sont pas à prendre à la légère. 
            Elles sont là pour donner le temps et les moyens à l’organisme de se rééquilibrer plus facilement. 
            Elles sont bien sûr propres à chaque animal et adaptées à chaque propriétaire.        
        </p>
        
